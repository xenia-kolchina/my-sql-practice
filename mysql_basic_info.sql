/* 
  СПРАВОЧНИК ПО ФУНКЦИЯМ ВРЕМЕНИ В MYSQL (ДЛЯ SQL ACADEMY)
  В MySQL работа с разницей во времени отличается от PostgreSQL:
  здесь нельзя просто вычитать даты оператором "-", нужно использовать функции.
*/
-- 1. TIMEDIFF(expr1, expr2)
-- Возвращает результат в формате времени (HH:MM:SS).
-- Работает как "expr1 - expr2". Максимальный диапазон: от -838:59:59 до 838:59:59.
-- Идеально подходит, когда нужен именно наглядный формат "часы:минуты:секунды".
SELECT TIMEDIFF('2023-10-01 12:00:00', '2023-10-01 10:30:00') AS diff; 
-- Результат: 01:30:00 (1 час 30 минут)

-- 2. TIMESTAMPDIFF(unit, datetime1, datetime2)
-- Возвращает разницу в целых числах в указанном периоде (unit).
-- Работает как "datetime2 - datetime1" (обратите внимание на порядок аргументов!).
-- Доступные unit: SECOND, MINUTE, HOUR, DAY, WEEK, MONTH, QUARTER, YEAR.
-- Это самая универсальная функция для расчетов (например, возраст или время полета в минутах).
SELECT TIMESTAMPDIFF(MINUTE, '2023-10-01 10:30:00', '2023-10-01 12:00:00') AS minutes;
-- Результат: 90 (ровно 90 минут)
SELECT TIMESTAMPDIFF(YEAR, '1995-05-15', CURDATE()) AS age;
-- Результат: количество полных лет

-- 3. TIMESTAMPADD(unit, interval, datetime)
-- Добавляет к дате или времени определенный интервал.
-- Аналог Postgres: "datetime + INTERVAL '1 day'".
-- Удобно для вычисления дедлайнов или времени прилета с учетом задержки.
SELECT TIMESTAMPADD(HOUR, 3, '2023-10-01 12:00:00') AS arrival_delayed;
-- Результат: 2023-10-01 15:00:00

